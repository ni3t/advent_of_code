# --- Day 4: Ceres Search ---
#
# "Looks like the Chief's not here. Next!" One of The Historians pulls out a device
# and pushes the only button on it. After a brief flash, you recognize the interior
# of the Ceres monitoring station!
#
# As the search for the Chief continues, a small Elf who lives on the station tugs on
# your shirt; she'd like to know if you could help her with her word search (your puzzle
# input). She only has to find one word: XMAS.
#
# This word search allows words to be horizontal, vertical, diagonal, written backwards,
# or even overlapping other words. It's a little unusual, though, as you don't merely
# need to find one instance of XMAS - you need to find all of them. Here are a few ways
# XMAS might appear, where irrelevant characters have been replaced with .:
#
# 
#
ex1 = <<~T
  ..X...
  .SAMX.
  .A..A.
  XMAS.S
  .X....
T
#
# The actual word search will be full of letters instead. For example:
#
ex2 = <<~T
  MMMSXXMASM
  MSAMXMSMSA
  AMXSXMAAMM
  MSAMASMSMX
  XMASAMXAMM
  XXAMMXXAMA
  SMSMSASXSS
  SAXAMASAAA
  MAMMMXMMMM
  MXMXAXMASX
T
#
# In this word search, XMAS occurs a total of 18 times; here's the same word search
# again, but where letters not involved in any XMAS have been replaced with .:
#
ex3 = <<~T
  ....XXMAS.
  .SAMXMS...
  ...S..A...
  ..A.A.MS.X
  XMASAMX.MM
  X.....XA.A
  S.S.S.S.SS
  .A.A.A.A.A
  ..M.M.M.MM
  .X.X.XMASX
T
#
# Take a look at the little Elf's word search. How many times does XMAS appear?

# --- Part Two ---
#
# The Elf looks quizzically at you. Did you misunderstand the assignment?
#
# Looking for the instructions, you flip over the word search to find that this isn't
# actually an XMAS puzzle; it's an X-MAS puzzle in which you're supposed to find two
# MAS in the shape of an X. One way to achieve that is like this:
#
ex1 = <<~T
  M.S
  .A.
  M.S
T
#
# Irrelevant characters have again been replaced with . in the above diagram. Within
# the X, each MAS can be written forwards or backwards.
#
# Here's the same example from before, but this time all of the X-MASes have been kept
# instead:
#
# ex2 = <<~T
#   .M.S......
#   ..A..MSMS.
#   .M.S.MAA..
#   ..A.ASMSM.
#   .M.S.M....
#   ..........
#   S.S.S.S.S.
#   .A.A.A.A..
#   M.M.M.M.M.
#   ..........
# T
#
# In this example, an X-MAS appears 9 times.
#
# Flip the word search from the instructions back over to the word search side and try
# again. How many times does an X-MAS appear?

@data = DATA.each_line.map(&:chomp)
@ex = ex3.each_line.map(&:chomp)

@arr = []

@data.each do |line|
  @arr << line.split('')
end

count = 0

@arr.each.with_index do |line,y|
  line.each.with_index do |char,x|
    next if char != "X"
    # p "X found at #{x},#{y}"
    # check right
    if x + 3 < line.size
      if line[x+1] == "M" && line[x+2] == "A" && line[x+3] == "S"
        count += 1
      end
    end
    # check left
    if x - 3 >= 0
      if line[x-1] == "M" && line[x-2] == "A" && line[x-3] == "S"
        count += 1
      end
    end
    # check down
    if y + 3 < @arr.size
      if @arr[y+1][x] == "M" && @arr[y+2][x] == "A" && @arr[y+3][x] == "S"
        count += 1
      end
    end
    # check up
    if y - 3 >= 0
      if @arr[y-1][x] == "M" && @arr[y-2][x] == "A" && @arr[y-3][x] == "S"
        count += 1
      end
    end
    # check diagonal down right
    if x + 3 < line.size && y + 3 < @arr.size
      if @arr[y+1][x+1] == "M" && @arr[y+2][x+2] == "A" && @arr[y+3][x+3] == "S"
        count += 1
      end
    end
    # check diagonal down left
    if x - 3 >= 0 && y + 3 < @arr.size
      if @arr[y+1][x-1] == "M" && @arr[y+2][x-2] == "A" && @arr[y+3][x-3] == "S"
        count += 1
      end
    end
    # check diagonal up right
    if x + 3 < line.size && y - 3 >= 0
      if @arr[y-1][x+1] == "M" && @arr[y-2][x+2] == "A" && @arr[y-3][x+3] == "S"
        count += 1
      end
    end
    # check diagonal up left
    if x - 3 >= 0 && y - 3 >= 0
      if @arr[y-1][x-1] == "M" && @arr[y-2][x-2] == "A" && @arr[y-3][x-3] == "S"
        count += 1
      end
    end
  end
end

puts count

count2 = 0

@arr.each.with_index do |line,y|
  line.each.with_index do |char,x|
    next unless char == "A"
    next unless y > 0 && y < @arr.size - 2
    next unless x > 0 && x < line.size - 2
    next unless [@arr[y-1][x-1], @arr[y+1][x+1]].sort.join == "MS"
    next unless [@arr[y-1][x+1], @arr[y+1][x-1]].sort.join == "MS"
    count2 += 1
  end
end

puts count2

# Part 2
p input.map.with_index.sum { |row,y|
