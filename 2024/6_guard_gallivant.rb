class Grid
  attr_reader :data, :guard, :status

  def initialize(init=[])
    @initial_state = init.dup.clone.map(&:freeze).freeze
    @data = init.map(&:dup)
    @obstacles = []
    @guard = nil
    @round = 0
    @status = :new
  end

  def <<(line)
    @initial_state << line
    @data << line
  end

  def prepare_game
    @initial_state.each_with_index do |line, y|
      line.each_with_index do |char, x|
        case char
        when '#'
          @obstacles << [x,y]
        when '^'
          @guard = Guard.new(x,y,:up)
        when '>'
          @guard = Guard.new(x,y,:right)
        when 'v'
          @guard = Guard.new(x,y,:down)
        when '<'
          @guard = Guard.new(x,y,:left)
        end
      end
    end
    self
  end

  def play_round(visual: false, track: false)
    if @guard.next_location.first.negative? || @guard.next_location.last.negative? ||
     @guard.next_location.first >= @data.first.size || @guard.next_location.last >= @data.size
      return (@status = :out_of_bounds)
    end
    @guard.turn_right until !@obstacles.include?(@guard.next_location)
    @data[@guard.y][@guard.x] = track ? "X" : '.'
    @guard.move
    if @guard.looping?
      return (@status = :looping)
    end
    @data[@guard.y][@guard.x] = @guard.pretty_dir
    @round += 1
    print if visual
    (@status = :continue)
  end

  def print
    puts "-----"
    puts "Round: #{@round}"
    puts "Status: #{@status}"
    puts @guard
    puts @data.map(&:join).join("\n")
  end

  def play(rounds:0, visual: false, track: false)
    if rounds.zero?
      @status = :continue
      until @status != :continue do
        play_round(visual: visual, track: track)
      end
    else
      rounds.times do
        play_round(visual: visual, track: track)
      end
    end
    self
  end

  def add_temporary_block(x,y)
    @data[y][x] = 'O'
    @obstacles << [x,y]
    self
  end

  def reset
    @round = 0
    @data = @initial_state
    prepare_game
    print
    self
  end
end

class Guard
  attr_reader :x, :y, :dir, :visited

  def initialize(x,y,dir)
    @x=x
    @y=y
    @dir=dir
    @visited = Set.new([[x,y,dir]])
    @looping = false
  end

  def move
    case @dir
    when :up; @y -= 1
    when :right; @x += 1
    when :down; @y += 1
    when :left; @x -= 1
    end
    if @visited.include?([@x,@y,@dir])
      @looping = true
    else
      @visited << [@x,@y, @dir]
    end
  end

  def pretty_dir
    case @dir
    when :up; '^'
    when :right; '>'
    when :down; 'v'
    when :left; '<'
    end
  end

  def next_location
    case @dir
    when :up; [@x, @y-1]
    when :right; [@x+1, @y]
    when :down; [@x, @y+1]
    when :left; [@x-1, @y]
    end
  end

  def turn_right
    case @dir
    when :up; @dir = :right
    when :right; @dir = :down
    when :down; @dir = :left
    when :left; @dir = :up
    end
  end
  
  def to_s
    "Guard at (#{@x},#{@y}) facing #{@dir}"
  end

  def looping?
    @looping
  end
end

